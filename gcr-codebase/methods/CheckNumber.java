import java.util.Scanner;

class CheckNumber {

    static int check(int n) {
        if (n > 0) return 1;
        if (n < 0) return -1;
        return 0;
    }

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter number: ");
        int n = sc.nextInt();

        System.out.println("Result = " + check(n));
        sc.close();
    }
}
 